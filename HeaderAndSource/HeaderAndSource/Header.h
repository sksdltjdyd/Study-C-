// 사용자 정의 헤더 : 사용자가 직접 정의한 헤더파일. 일반적으로 .h 확장자를 가지며, 함수 선언, 클래스 정의, 상수 정의 등을 포함한다. 
// 사용자 정의 헤더는 다른 소스파일에서 재사용할 수 있다. 사용자 정의 헤더는 <> 대신 ""를 사용하여 포함한다. 예를 들어, #include "myheader.h"와 같이 사용한다

//cstdio : 표준 입출력 헤더. 콘솔 입출력과 파일 입출력을 위한 함수들을 제공한다. Printf, scanf, fseek, fopen, fclose 등의 함수가 포함되어 있다.

#include <cstdio> // 헤더파일을 포함하는 방법. #include 지시어를 사용하여 헤더파일을 포함한다
#include <iostream> // C++ 표준 입출력 헤더. cin, cout, cerr, clog 등의 객체를 제공한다
#include <cmath> // 수학 함수 헤더. 수학 관련 함수들을 제공한다. pow, sqrt, sin, cos, tan 등의 함수가 포함되어 있다
#include <cstdlib> // C 표준 라이브러리 헤더. 메모리 할당, 프로세스 제어, 변환 등의 함수들을 제공한다. malloc, free, exit, atoi 등의 함수가 포함되어 있다
#include <cstring> // 문자열 처리 헤더. 문자열 관련 함수들을 제공한다. strcpy, strlen, strcmp 등의 함수가 포함되어 있다
#include <ctime> // 시간 관련 헤더. 시간과 날짜를 처리하는 함수들을 제공한다. time, clock, localtime 등의 함수가 포함되어 있다

/*
소스파일과 헤더파일의 상관관계
1. 헤더파일은 소스파일에 포함되어야 한다.
2. 헤더파일은 소스파일에 여러 번 포함될 수 있다.
3. 헤더파일은 소스파일에 포함될 때마다 컴파일러가 읽어야 한다.
4. 헤더파일은 소스파일에 포함될 때마다 컴파일러가 읽지 않도록 해야 한다.
*/

int add(int a, int b); // 함수 선언. 헤더파일에 함수의 선언을 작성한다. 함수의 정의는 소스파일에 작성한다